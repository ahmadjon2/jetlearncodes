def fibonachi_s(n):
    if n == 0 or n == 1:
        return n
    else:
        return fibonachi_s(n-1)+fibonachi_s(n-2)
print (fibonachi_s(8))

def factorial(f):
    if f == 1 :
        return 1
    else:
        return f*factorial(f-1)
